package com.trainingvti.dto;

public class GroupFilter {
	private Short minID;
	private Short maxID;
	
	public GroupFilter() {
		super();
	}

	public Short getMinID() {
		return minID;
	}

	public void setMinID(Short minID) {
		this.minID = minID;
	}

	public Short getMaxID() {
		return maxID;
	}

	public void setMaxID(Short maxID) {
		this.maxID = maxID;
	}
	 
}
